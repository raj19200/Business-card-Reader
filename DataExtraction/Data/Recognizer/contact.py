import re

def is_contact(txt):
    m = re.findall(r'((?:(?:\+*)(?:(?:0[ -]+)*|(?:91[ -])*))(?:\d{12}|\d{10}|\d{3}[\ -]\d{3}[\ -]\d{4}|\d{5}[\ -]\d{5})|\d{5}(?:[- ]*)\d{6}|\d{4}[\ -]\d{2}[\ -]\d{4})',txt)
    p = re.findall(r'((?:(?:\+|0{0,2})(?:\s*[\ -]\s*)?|[0])?(?:\d{3}[\ -]\d{7}|\d{4}[\ -]\d{6}))(?![^91]{2}[\ -]\d{8})',txt)
    m.extend(p)
    return m
